import { getSoldPlans } from '@/app/apiActions/admin/getSoldPlans'
import NoData from '@/utils/NoData';
import React from 'react'
import SalesTable from './SoldTable';

export default async function SalesHistory() {
    const { status, data } = await getSoldPlans();

    if (status !== 200) {
        return <NoData text={"Sold Plan not found!"} />
    }


    return <SalesTable soldsData={data} />

}
